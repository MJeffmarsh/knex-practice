function groceriesAdded(daysAgo) {
  knexInstance
    .select('name')
    .count('date_added AS added')
    .where(
      'date_added',
      '>',
      knexInstance.raw(`now() - '?? days'::INTERVAL`, daysAgo)
    )
    .from('shopping_list')
    .groupBy('name')
    .orderBy([{ column: 'name', order: 'ASC' }])
    .then(result => {
      console.log(result);
    });
}

